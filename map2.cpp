#include<iostream>
#include<map>
using namespace std;

int main()
{
        map<int,string> student;

        student[1]="Alice";
        student[2]="Bob";
        student[3]="John";
        student[4]="Steve";
        student[5]="Jake";

        //declare a map iterator
        map<int,string>::iterator itr;

        //use iterator to display map student elements
        for(itr=student.begin();itr!=student.end();itr++)
        {
                cout<<itr->first<< " - " <<itr->second<<endl;
        }

        return 0;
}
