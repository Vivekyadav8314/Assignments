#include<iostream>
using namespace std;

template <typename T>
void swapvalues(T& a, T& b)
{
        T temp=a;
        a=b;
        b=temp;
}

int main()
{
        int x=5,y=10;
        cout<<"Before Swap: x = "<< x << ", y = "<< y <<endl;
        swapvalues(x,y);
        cout<<"After Swap: x = "<< x <<" , y = "<< y <<endl;

        double a=1.5,b=6.8;
        cout<<"Before Swap: a = "<< a << ", b ="<< b <<endl;
        swapvalues(a,b);
        cout<<"After Swap: a = "<< a << ", b ="<< b <<endl;

        return 0;
}
